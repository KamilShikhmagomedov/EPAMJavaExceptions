public class NoFacultetsAvailableException extends Exception {
    public NoFacultetsAvailableException() {
    }

    public NoFacultetsAvailableException(String message) {
        super(message);
    }

    public NoFacultetsAvailableException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoFacultetsAvailableException(Throwable cause) {
        super(cause);
    }
}
