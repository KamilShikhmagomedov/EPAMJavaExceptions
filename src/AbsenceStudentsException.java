public class AbsenceStudentsException extends Exception {
    public AbsenceStudentsException() {
    }

    public AbsenceStudentsException(String message) {
        super(message);
    }

    public AbsenceStudentsException(String message, Throwable cause) {
        super(message, cause);
    }

    public AbsenceStudentsException(Throwable cause) {
        super(cause);
    }
}
